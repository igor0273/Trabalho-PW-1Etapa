package edu.br.ifsul.converters;

import br.edu.ifsul.model.Empresa;
import java.io.Serializable;
import javax.enterprise.context.RequestScoped;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import javax.inject.Named;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;

/**
 *
 * @author igor.rocha
 */
@Named(value = "converterEmpresa")
@RequestScoped
public class ConverterEmpresa implements Serializable, Converter{

    @PersistenceContext(unitName = "PW2022-2-WebPU")
    private EntityManager em;
    
    // da tela para o objeto
    @Override
    public Object getAsObject(FacesContext fc, UIComponent uic, String string) {
        if (string == null || string.equals("Selecione um registro")){
            return null;
        }
        return em.find(Empresa.class, Integer.parseInt(string));
    }

    // do objeto para a tela
    @Override
    public String getAsString(FacesContext fc, UIComponent uic, Object t) {
        if (t == null){
            return null;
        }
        Empresa obj = (Empresa) t;
        return obj.getId().toString();
    }

}
